// Script pour créer des icônes de placeholder
// Exécuter : node create-icons.js

const fs = require('fs');
const path = require('path');

// PNG minimal 1x1 pixel transparent
const minimalPng = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk header
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1 pixels
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, // bit depth, color type, etc
  0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41, // IDAT chunk
  0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00, // compressed data
  0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, // CRC
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82, // IEND CRC
]);

const icons = ['icon.png', 'splash-icon.png', 'adaptive-icon.png', 'favicon.png'];

icons.forEach(icon => {
  const iconPath = path.join(__dirname, icon);
  fs.writeFileSync(iconPath, minimalPng);
  console.log(`Created: ${icon}`);
});

console.log('Done! Replace these with your actual icons.');
